public class ExerciseSet
{
    private int weight;
    private int reps;

    public ExerciseSet(int weight, int reps)
    {
        this.weight = weight;
        this.reps = reps;
    }

    public int getWeight()
    {
        return weight;
    }

    public int getReps()
    {
        return reps;
    }
}